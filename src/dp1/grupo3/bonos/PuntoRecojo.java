package dp1.grupo3.bonos;

public class PuntoRecojo {
    private int id_punto_reocojo;
    private int ubigeo;
    private int turno;
    private int capacidad;

    public int getId_punto_reocojo() {
        return id_punto_reocojo;
    }

    public void setId_punto_reocojo(int id_punto_reocojo) {
        this.id_punto_reocojo = id_punto_reocojo;
    }

    public int getUbigeo() {
        return ubigeo;
    }

    public void setUbigeo(int ubigeo) {
        this.ubigeo = ubigeo;
    }

    public int getTurno() {
        return turno;
    }

    public void setTurno(int turno) {
        this.turno = turno;
    }

    public int getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(int capacidad) {
        this.capacidad = capacidad;
    }
}
